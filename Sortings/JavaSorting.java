public class JavaSorting {

      public static void printArr(Integer arr[]){
            for (int i = 0; i < arr.length; i++) {
                  System.out.print(arr[i] + " ");
            }
            System.out.println();
      }

      public static void main(String args[]){
            Integer arr[] = {5,4,8,6,2,9};
            printArr(arr);
      }
}

